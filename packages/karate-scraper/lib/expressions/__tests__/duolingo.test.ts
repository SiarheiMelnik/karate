import * as scraper from '../../scraper'
import {
  expression as profileExpression,
  url as profileAddr,
} from '../duolingo/profile'

test('Scrape duolingo', async () => {
  const url = profileAddr({ userName: 'siarheimel' })
  const expression = profileExpression()
  const data = await scraper.scrape({ url, expression })
  const value = data.result.value
  expect(JSON.parse(value)).toHaveLength(1)
})
